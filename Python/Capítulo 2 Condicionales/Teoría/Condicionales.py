'''

*Compara dos valores                                            Condicionales
*Determina los valores y ejecuta una acción u otra              if, else, elif

'''

numero=float(input('Digite un número: '))

if numero>0:
    print('El número es positivo')
    
elif numero==0:
    print('El número es 0')

else:
    print('El número es negativo')

