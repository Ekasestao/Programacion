#include<iostream>

using namespace std;

int main(){
    int entero=15; //Variable tipo entero
    float flotante=20.25; //Variable tipo real
    double mayor=16.3456; //Variable tipo double es como float pero guarda números más extensos
    char letra='a'; //Variable tipo caracter (solo un caracter)
    cout<<entero;
    cout<<'\n'; //'\n' es como en Python salto de linea
    cout<<flotante;
    cout<<'\n';
    cout<<mayor;
    cout<<'\n';
    cout<<letra;
    return 0;
}